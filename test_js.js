// Load notes
function load() {
  try { const s = localStorage.getItem(SK); notes = s ? JSON.parse(s) : []; } catch(e) { notes = []; }
  if (notes.length === 0) {
    const now = new Date().toISOString();
    PRELOADED.forEach(n => { notes.push({...n, created: now, updated: now}); });
    save();
  }
}

function save() { localStorage.setItem(SK, JSON.stringify(notes)); }

function toast(msg) {
  const t = document.getElementById('toast');
  t.textContent = msg; t.style.display = 'block';
  setTimeout(() => t.style.display = 'none', 2000);
}

// Render sidebar
function renderList(filter = '') {
  const list = document.getElementById('notes-list');
  const filtered = filter ? notes.filter(n => 
    n.title.toLowerCase().includes(filter.toLowerCase()) || 
    n.content.toLowerCase().includes(filter.toLowerCase()) ||
    (n.tags && n.tags.some(t => t.toLowerCase().includes(filter.toLowerCase())))
  ) : notes;
  
  const sorted = filtered.sort((a, b) => new Date(b.updated) - new Date(a.updated));
  
  list.innerHTML = sorted.map(n => {
    const preview = n.content.substring(0, 50).replace(/\n/g, ' ');
    const date = new Date(n.updated).toLocaleDateString();
    const catBadge = `<span class="category-badge cat-${n.category}">${n.category}</span>`;
    return `<div class="note-item ${n.id === activeId ? 'active' : ''}" onclick="openNote(${n.id})">
      <div class="ni-title">${n.title || 'Untitled'}${catBadge}</div>
      <div class="ni-date">${date}</div>
      <div class="ni-preview">${preview}</div>
    </div>`;
  }).join('');

  // Stats
  document.getElementById('stats').innerHTML = `${notes.length} notes | ${notes.reduce((s,n) => s + n.content.length, 0)} chars`;
}

// Open note
function openNote(id) {
  activeId = id;
  const note = notes.find(n => n.id === id);
  if (!note) return;
  document.getElementById('note-title').value = note.title;
  document.getElementById('note-content').value = note.content;
  document.getElementById('category').value = note.category || 'general';
  renderTags(note.tags || []);
  renderList();
}

// New note
function newNote() {
  const note = {
    id: Date.now(),
    title: '',
    content: '',
    category: 'general',
    tags: [],
    created: new Date().toISOString(),
    updated: new Date().toISOString()
  };
  notes.unshift(note);
  save();
  openNote(note.id);
  document.getElementById('note-title').focus();
  toast('New note created');
}

// Save note
function saveNote() {
  if (!activeId) return;
  const note = notes.find(n => n.id === activeId);
  if (!note) return;
  note.title = document.getElementById('note-title').value;
  note.content = document.getElementById('note-content').value;
  note.category = document.getElementById('category').value;
  note.updated = new Date().toISOString();
  save();
  renderList();
  toast('Saved ✓');
}

// Auto-save with debounce
function autoSave() {
  clearTimeout(saveTimer);
  saveTimer = setTimeout(saveNote, 1000);
}

// Delete note
function deleteNote() {
  if (!activeId) return;
  if (!confirm('Delete this note?')) return;
  notes = notes.filter(n => n.id !== activeId);
  activeId = null;
  save();
  document.getElementById('note-title').value = '';
  document.getElementById('note-content').value = '';
  renderList();
  toast('Note deleted');
}

// Category
